    private void clicGuardarComo(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clicGuardarComo
        JFileChooser fc = new JFileChooser(".");
        int opcion = fc.showSaveDialog(this);        
        if(opcion == JFileChooser.APPROVE_OPTION )
        {
            clicGuardar.setEnabled(true);
            btnGuardar.setEnabled(true);
            try {
                rutaArchivo = funcion.guardarComo(fc.getSelectedFile(), jEditorPane1.getText());
            } catch (IOException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }
        }    
        
        
    }//GEN-LAST:event_clicGuardarComo

