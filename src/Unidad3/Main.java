    private void clicCerrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clicCerrarActionPerformed
        
        //Si = 0
        //No = 1
        //Cancelar = 2
        JOptionPane.showMessageDialog(null, funcion.rutaArchivo);
        if(!"".equals(rutaArchivo))
        {
            
            int respuesta = JOptionPane.showConfirmDialog(null, "¿Desea guardar antes de cerrar?");
            if(respuesta==0)
            {
                try {
                    funcion.guardar(jEditorPane1.getText(), 0);
                    } catch (FileNotFoundException ex) {
                        Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
                    }
                jEditorPane1.setText("");
            }
            else if(respuesta == 1)
                jEditorPane1.setText("");
            clicGuardar.setEnabled(false);
            btnGuardar.setEnabled(false);
            
        }
    }//GEN-LAST:event_clicCerrarActionPerformed

